<h3>Police use-of-force</h3>

<p>Use-of-force data in Minneapolis and Minnesota in general got really sketchy and mostly unusable after 2020. New systems for tracking police force-use are under development, and this section will be updated as those efforts evolve.</p>

<p>In Minnesota, police have killed at least 235 people since 2000.</p>

